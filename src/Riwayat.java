    public void loadDataRiwayat() {
        DefaultTableModel model = (DefaultTableModel) tblRiwayat.getModel();
        model.setRowCount(0);

        try (Connection conn = koneksi.getConnection()) {
            String sql = "SELECT t.id_transaksi, t.tanggal_transaksi, " +
                         "IFNULL(p.nama, 'Umum') as pelanggan, " +
                         "IFNULL(u.username, 'Admin') as kasir, " +
                         "t.total_harga " +
                         "FROM transaksi t " +
                         "LEFT JOIN user u ON t.id_user = u.id_user " +
                         "LEFT JOIN pelanggan p ON t.id_pelanggan = p.id_pelanggan " +
                         "ORDER BY t.id_transaksi DESC";

            PreparedStatement ps = conn.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("id_transaksi"),
                    rs.getString("tanggal_transaksi"),
                    rs.getString("pelanggan"),
                    rs.getString("kasir"),
                    rs.getDouble("total_harga")
                });
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat riwayat: " + e.getMessage());
        }
    }

