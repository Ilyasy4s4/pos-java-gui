    private void tblRiwayatMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblRiwayatMouseClicked
        // TODO add your handling code here:
        int row = tblRiwayat.getSelectedRow();
        if (row != -1) {
            String idTrans = tblRiwayat.getValueAt(row, 0).toString();
            
            DefaultTableModel modelDetail = (DefaultTableModel) tblDetail.getModel();
            modelDetail.setRowCount(0);

            try (Connection conn = koneksi.getConnection()) {
                String sql = "SELECT b.nama_barang, dt.jumlah, dt.harga_satuan, dt.sub_total " +
                             "FROM detail_transaksi dt " +
                             "JOIN barang b ON dt.id_barang = b.id_barang " +
                             "WHERE dt.id_transaksi = ?";
                
                PreparedStatement ps = conn.prepareStatement(sql);
                ps.setString(1, idTrans);
                ResultSet rs = ps.executeQuery();

                while (rs.next()) {
                    modelDetail.addRow(new Object[]{
                        rs.getString("nama_barang"),
                        rs.getInt("jumlah"),
                        rs.getDouble("harga_satuan"),
                        rs.getDouble("sub_total")
                    });
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Gagal memuat rincian: " + e.getMessage());
            }
        }
    
    }//GEN-LAST:event_tblRiwayatMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
                                             
    // Ambil input dari textfield dan simpan ke variabel 'input'
    String input = jTextField1.getText().trim();
    
    // Jika kolom kosong, tampilkan semua data
    if (input.isEmpty()) {
        loadDataRiwayat();
        return;
    }

    DefaultTableModel model = (DefaultTableModel) tblRiwayat.getModel();
    model.setRowCount(0); 

    try (Connection conn = koneksi.getConnection()) {
        String sql;
        PreparedStatement ps;

        // Cek apakah input adalah angka murni (untuk ID Transaksi)
        if (input.matches("\\d+")) { 
            // Query ID Transaksi harus SAMA PERSIS (=) agar tidak memunculkan semua data
            sql = "SELECT t.id_transaksi, t.tanggal_transaksi, " +
                  "IFNULL(p.nama, 'Umum') as pelanggan, " +
                  "IFNULL(u.username, 'Admin') as kasir, t.total_harga " +
                  "FROM transaksi t " +
                  "LEFT JOIN user u ON t.id_user = u.id_user " +
                  "LEFT JOIN pelanggan p ON t.id_pelanggan = p.id_pelanggan " +
                  "WHERE t.id_transaksi = ?"; 
            ps = conn.prepareStatement(sql);
            ps.setString(1, input);
        } else {
            // Jika mengandung karakter lain (seperti -), cari berdasarkan Tanggal
            sql = "SELECT t.id_transaksi, t.tanggal_transaksi, " +
                  "IFNULL(p.nama, 'Umum') as pelanggan, " +
                  "IFNULL(u.username, 'Admin') as kasir, t.total_harga " +
                  "FROM transaksi t " +
                  "LEFT JOIN user u ON t.id_user = u.id_user " +
                  "LEFT JOIN pelanggan p ON t.id_pelanggan = p.id_pelanggan " +
                  "WHERE t.tanggal_transaksi LIKE ? " +
                  "ORDER BY t.id_transaksi DESC";
            ps = conn.prepareStatement(sql);
            ps.setString(1, "%" + input + "%");
        }

        ResultSet rs = ps.executeQuery();
        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id_transaksi"),
                rs.getString("tanggal_transaksi"),
                rs.getString("pelanggan"),
                rs.getString("kasir"),
                rs.getDouble("total_harga")
            });
        }
        
        // Jika tidak ada data yang ditemukan
        if (model.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Data '" + input + "' tidak ditemukan.");
            loadDataRiwayat();
        }
        
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error Database: " + e.getMessage());
    }


    }//GEN-LAST:event_jButton1ActionPerformed

