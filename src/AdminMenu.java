    private void b_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_editActionPerformed
                                    
    int selectedRow = jTable1.getSelectedRow();
    if (selectedRow != -1) {
        // Ambil ID dari kolom pertama tabel
        String id_user = jTable1.getValueAt(selectedRow, 0).toString();

        // Panggil frame EditUser dengan ID tersebut
        EditUser editForm = new EditUser(id_user);
        editForm.setVisible(true);
        editForm.setLocationRelativeTo(null); // tampil di tengah layar
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Pilih data yang ingin diedit terlebih dahulu!");
    }


    }//GEN-LAST:event_b_editActionPerformed

    private void B_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_B_hapusActionPerformed
        int baris = jTable1.getSelectedRow(); // tabelUser = JTable kamu
        if (baris != -1) { // Pastikan ada baris yang dipilih
             String id = jTable1.getValueAt(baris, 0).toString(); // ambil id_user dari kolom pertama
            int konfirmasi = JOptionPane.showConfirmDialog(this, 
                "Apakah Anda yakin ingin menghapus data dengan ID: " + id + " ?", 
                "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);

        if (konfirmasi == JOptionPane.YES_OPTION) {
            try {
                Connection conn = koneksi.getConnection(); // panggil class koneksi kamu
                String sql = "DELETE FROM user WHERE id_user = ?";
                PreparedStatement ps = conn.prepareStatement(sql);
                ps.setString(1, id);
                ps.executeUpdate();
                
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
                
                
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Gagal menghapus data: " + e.getMessage());
            }
        }
    } else {
        JOptionPane.showMessageDialog(this, "Pilih baris data yang ingin dihapus terlebih dahulu!");
    }
// TODO add your handling code here:
    }//GEN-LAST:event_B_hapusActionPerformed

