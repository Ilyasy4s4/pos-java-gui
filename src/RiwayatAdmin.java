    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:                                      
    String namaKasir = cbKasir.getSelectedItem().toString();
    
    if (namaKasir.equals("-- Pilih Kasir --")) {
        JOptionPane.showMessageDialog(this, "Silakan pilih nama kasir!");
        return;
    }

    DefaultTableModel model = (DefaultTableModel) tblRiwayatAdmin.getModel();
    model.setRowCount(0); // Bersihkan tabel

    try (Connection conn = koneksi.getConnection()) {
        // Query untuk mengambil riwayat berdasarkan kasir yang dipilih
        String sql = "SELECT t.id_transaksi, t.tanggal_transaksi, IFNULL(p.nama, 'Umum') as pelanggan, " +
                     "t.total_harga, t.bayar, t.kembalian " +
                     "FROM transaksi t " +
                     "JOIN user u ON t.id_user = u.id_user " +
                     "LEFT JOIN pelanggan p ON t.id_pelanggan = p.id_pelanggan " +
                     "WHERE u.username= ?";
        
        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setString(1, namaKasir);
        ResultSet rs = ps.executeQuery();

        double totalOmzet = 0;
        int jumlahTransaksi = 0;

        while (rs.next()) {
            double harga = rs.getDouble("total_harga");
            totalOmzet += harga;
            jumlahTransaksi++;

            model.addRow(new Object[]{
                rs.getInt("id_transaksi"),
                rs.getString("tanggal_transaksi"),
                rs.getString("pelanggan"),
                harga,
                rs.getDouble("bayar"),
                rs.getDouble("kembalian")
            });
        }

        // Hitung Rata-rata
        double rataRata = (jumlahTransaksi > 0) ? totalOmzet / jumlahTransaksi : 0;

        // Update Label Hasil (Poin 41)
        lblTotalOmzet.setText("Rp " + String.format("%,.0f", totalOmzet));
        lblTotalTransaksi.setText(String.valueOf(jumlahTransaksi));
        lblRataRata.setText("Rp " + String.format("%,.0f", rataRata));

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }

    }//GEN-LAST:event_jButton1ActionPerformed

