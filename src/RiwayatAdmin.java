    private void loadComboKasir() {
    cbKasir.removeAllItems();
    cbKasir.addItem("-- Pilih Kasir --");
    try (Connection conn = koneksi.getConnection()) {
        // Mengambil daftar kasir dari tabel user
        String sql = "SELECT username FROM user WHERE role = 'Kasir'";
        Statement st = conn.createStatement();
        ResultSet rs = st.executeQuery(sql);
        while (rs.next()) {
            cbKasir.addItem(rs.getString("username"));
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Gagal memuat daftar kasir: " + e.getMessage());
    }
}
    

