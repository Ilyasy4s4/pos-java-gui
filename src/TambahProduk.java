    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
    java.sql.Connection conn = koneksi.getConnection();

    // ðŸ”¹ Ambil id_kategori terakhir dari tabel kategori
    String getKategori = "SELECT id_kategori FROM kategori ORDER BY id_kategori DESC LIMIT 1";
    java.sql.Statement stmt = conn.createStatement();
    java.sql.ResultSet rsKat = stmt.executeQuery(getKategori);
    int id_kategori = 0;
    if (rsKat.next()) {
        id_kategori = rsKat.getInt("id_kategori");
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Belum ada kategori di database!");
        return;
    }
       
        String nama_barang = j_nama_barang.getText();
        String harga_beli = j_harga_beli.getText();
        String harga_jual = jTextField7.getText();
        String stok = jTextField6.getText();
        String satuan = jTextField8.getText();
        String diskon = j_Diskon.getText();
        
  
        if ( nama_barang.isEmpty() || stok.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Harap isi semua kolom!");
            return;
        }

        
            String sql = "INSERT INTO barang (nama_barang, id_kategori, harga_beli, harga_jual, stok, satuan, diskon) VALUES ( ?, ?, ?, ?, ?, ?, ?)";
            java.sql.PreparedStatement pst = conn.prepareStatement(sql,java.sql.Statement.RETURN_GENERATED_KEYS);

            
            pst.setString(1, nama_barang);
            pst.setInt(2, id_kategori);
            pst.setString(3, harga_beli);
            pst.setString(4, harga_jual);
            pst.setString(5, stok);
            pst.setString(6, satuan);
            pst.setString(7, diskon);
            pst.executeUpdate();

            ResultSet rs = pst.getGeneratedKeys();
            if (rs.next()) {
                int newId = rs.getInt(1);
                j_id_barang.setText(String.valueOf(newId));
            }

            javax.swing.JOptionPane.showMessageDialog(this, "Data barang berhasil disimpan!");

            // Setelah simpan, tutup frame Tambah
            this.dispose();

            // Refresh tabel di AdminMenu
            AdminMenu adminMenu = new AdminMenu();
            adminMenu.loadDataUser(); // fungsi ini nanti kita tambahkan di AdminMenu

        } catch (HeadlessException | SQLException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal menyimpan: " + e.getMessage());
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

