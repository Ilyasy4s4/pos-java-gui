    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            java.sql.Connection conn = koneksi.getConnection();

            String id_barang = j_id_barang.getText();
            String nama_barang = j_nama_barang.getText();
            String id_kategori = j_id_kategori.getText();
            String harga_beli = j_harga_beli.getText();
            String harga_jual = jTextField7.getText();
            String stok = jTextField6.getText();
            String satuan = jTextField8.getText();
            String diskon = j_Diskon.getText();

            if (nama_barang.isEmpty() || stok.isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this, "Harap isi semua kolom!");
                return;
            }

            String sql = "UPDATE barang SET nama_barang=?, id_kategori=?, harga_beli=?, harga_jual=?, stok=?, satuan=?, diskon=? WHERE id_barang=?";
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, nama_barang);
            pst.setString(2, id_kategori);
            pst.setString(3, harga_beli);
            pst.setString(4, harga_jual);
            pst.setString(5, stok);
            pst.setString(6, satuan);
            pst.setString(7, diskon);
            pst.setString(8, id_barang);

            int updated = pst.executeUpdate();
            if (updated > 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Data produk berhasil diperbarui!");
                this.dispose();
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Tidak ada data yang diubah!");
            }

        } catch (HeadlessException | SQLException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal mengubah data: " + e.getMessage());
        }
    

        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

