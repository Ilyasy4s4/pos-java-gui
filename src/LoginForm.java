    private void bt_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_loginActionPerformed
        // 1. Ambil input dari field
        String inputUsername = t_username.getText().trim();
        String inputPassword = t_password.getText().trim();

        // Hapus teks default "Username" atau "Password" jika pengguna belum mengetik
        if (inputUsername.equals("Username") || inputPassword.equals("Password")) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Masukkan Username dan Password Anda.",
                    "Peringatan",
                    javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

      
    String sql = "SELECT id_user, username, role FROM user WHERE username = ? AND password = ?";

    try (java.sql.Connection conn = koneksi.getConnection(); java.sql.PreparedStatement pst = conn.prepareStatement(sql)) {

    pst.setString(1, inputUsername);
    pst.setString(2, inputPassword);

    try (java.sql.ResultSet rs = pst.executeQuery()) {
        if (rs.next()) {
            // Menyimpan data user ke variabel instance
            this.userId = rs.getInt("id_user");
            this.userRole = rs.getString("role");
            this.username = rs.getString("username");

            String welcomeMessage = String.format("Login Berhasil! Selamat datang %s (ID: %d)",
                    this.username, this.userId); // ‚Üê INI JUGA SALAH, HARUSNYA userId BUKAN userRole

            javax.swing.JOptionPane.showMessageDialog(this,
                    welcomeMessage,
                    "Sukses",
                    javax.swing.JOptionPane.INFORMATION_MESSAGE);

            // Setelah login berhasil
            String role = rs.getString("role"); // ambil role dari database
            this.userRole = role; // simpan di variabel instance

            if (role.equalsIgnoreCase("admin")) {
            // üîπ Login sebagai admin
            MenuAdmin menuAdmin = new MenuAdmin(this); // kirim LoginForm ke MenuAdmin
            menuAdmin.setVisible(true);
            this.dispose(); // tutup form login

            } else if (role.equalsIgnoreCase("kasir")) {
            // üîπ Login sebagai kasir
            MenuForm menuKasir = new MenuForm(this); // kirim LoginForm ke MenuForm
            menuKasir.setVisible(true);
            this.dispose(); // tutup form login
            
            } else if (role.equalsIgnoreCase("owner")) {
            // JANGAN TULIS: dashboard Dashboard = ...
            // TULIS LANGSUNG:
            dashboard ownerDash = new dashboard(); // Pastikan nama variabel tidak bentrok dengan nama class
            ownerDash.setVisible(true);
            this.dispose();


            } else {
            JOptionPane.showMessageDialog(this,
            "Role tidak dikenali!",
            "Error",
            JOptionPane.ERROR_MESSAGE);
}

            
            
        } else {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Username atau Password salah. Coba lagi.",
                    "Gagal",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
            t_password.setText("Password");
            t_username.requestFocus();
            bersih();
        }
    } // ‚Üê INI TUTUP try ResultSet
} catch (java.sql.SQLException e) { // ‚Üê INI HARUS ADA CATCH-NYA
    javax.swing.JOptionPane.showMessageDialog(this,
            "Error Database: " + e.getMessage(),
            "Database Error",
            javax.swing.JOptionPane.ERROR_MESSAGE);
    logger.log(java.util.logging.Level.SEVERE, "Database error during login", e);
} // ‚Üê INI TUTUP try Connection


    }//GEN-LAST:event_bt_loginActionPerformed

